    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String re = rn.getText();
        int reg = Integer.parseInt(re);
        Connection conn = null;
        PreparedStatement pst1=null;
        ResultSet rs1=null;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            conn=DriverManager.getConnection("jdbc:mysql://localhost/fees_receipt","root","root");
            pst1=conn.prepareStatement("select * from fees where register_no = ?;");
          
            pst1.setInt(1, reg);
            rs1=pst1.executeQuery();
            while(rs1.next())
            {
                student.setText(rs1.getString("STUDENTS_NAME"));
                dy.setText(rs1.getString("DEPARTMENT_YEAR"));
                ad.setText(rs1.getString("ADDRESS"));
                Q.setText(rs1.getString("QUOTA"));
                fa.setText(rs1.getString("fees"));
                date.setText(rs1.getString("date"));
                rno.setText(rs1.getString("reciept_no"));
                
            }
        }
        catch(Exception e){
              JOptionPane.showMessageDialog(null, e );
     
        }
        finally{
            try{
                    conn.close();
                    pst1.close();
                    rs1.close();
                }
        catch(Exception e)
        {
            
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

